export function calculateFluencyFeedback(score: number): string {
  if (score >= 95) {
    // 95점 이상
    return '아주 훌륭합니다! 발표에서 불필요한 추임새가 거의 사용되지 않았습니다. 자연스럽고 유창한 발표였습니다.';
  } else if (score >= 90) {
    // 90점 이상
    return '전반적으로 유창한 발표였습니다. 다만, ‘어, 음, 그’와 같은 추임새가 간헐적으로 사용되어 발표 흐름이 약간 끊기는 느낌이 있었습니다. 발표 전 문장을 미리 정리해보고, 핵심 문장 위주로 연습하면 더욱 자연스럽고 설득력 있는 발표가 될 수 있습니다.';
  } else if (score >= 82) {
    // 82점 이상
    return '발표 중 ‘어, 음, 그’와 같은 불필요한 추임새가 다소 사용되었습니다. 발표의 흐름이 살짝 끊기는 느낌이 있었지만, 내용 전달에는 큰 영향을 주지 않았습니다. 다음 발표에서는 문장을 더 명확하게 구성하는 연습을 해보는 것이 좋겠습니다.';
  } else if (score >= 65) {
    // 65점 이상
    return '발표에서 불필요한 추임새가 자주 등장하여 발표의 명확성이 다소 저하되었습니다. 발표를 듣는 동안 청중이 핵심 내용을 파악하는데 방해가 될 가능성이 있습니다. 발표 전 충분한 연습을 통해 자연스럽게 발표 내용을 연결하는 연습을 해보세요.';
  } else if (score > 0) {
    // 65점 미만
    return '발표에서 불필요한 추임새가 과도하게 사용되었습니다. 발표 흐름이 끊기고, 청중의 집중도가 낮아질 가능성이 높습니다. 발표 내용이 전달되기 어려울 수 있으므로, 명확하고 간결한 문장을 구성하는 연습이 필요합니다. 짧은 문장으로 말하는 연습을 하거나, 발표 전에 키워드를 미리 정리하여 불필요한 추임새를 줄여보세요.';
  } else {
    return '';
  }
}
