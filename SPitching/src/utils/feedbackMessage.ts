export function calculateFluencyFeedback(score: number): string {
  if (score >= 95) {
    // 95점 이상
    return '아주 훌륭합니다! 발표에서 불필요한 추임새가 거의 사용되지 않았습니다. 자연스럽고 유창한 발표였습니다.';
  } else if (score >= 90) {
    // 90점 이상
    return '전반적으로 유창한 발표였습니다. 다만, ‘어, 음, 그’와 같은 추임새가 간헐적으로 사용되어 발표 흐름이 약간 끊기는 느낌이 있었습니다. 발표 전 문장을 미리 정리해보고, 핵심 문장 위주로 연습하면 더욱 자연스럽고 설득력 있는 발표가 될 수 있습니다.';
  } else if (score >= 82) {
    // 82점 이상
    return '발표 중 ‘어, 음, 그’와 같은 불필요한 추임새가 다소 사용되었습니다. 발표의 흐름이 살짝 끊기는 느낌이 있었지만, 내용 전달에는 큰 영향을 주지 않았습니다. 다음 발표에서는 문장을 더 명확하게 구성하는 연습을 해보는 것이 좋겠습니다.';
  } else if (score >= 65) {
    // 65점 이상
    return '발표에서 불필요한 추임새가 자주 등장하여 발표의 명확성이 다소 저하되었습니다. 발표를 듣는 동안 청중이 핵심 내용을 파악하는데 방해가 될 가능성이 있습니다. 발표 전 충분한 연습을 통해 자연스럽게 발표 내용을 연결하는 연습을 해보세요.';
  } else if (score > 0) {
    // 65점 미만
    return '발표에서 불필요한 추임새가 과도하게 사용되었습니다. 발표 흐름이 끊기고, 청중의 집중도가 낮아질 가능성이 높습니다. 발표 내용이 전달되기 어려울 수 있으므로, 명확하고 간결한 문장을 구성하는 연습이 필요합니다. 짧은 문장으로 말하는 연습을 하거나, 발표 전에 키워드를 미리 정리하여 불필요한 추임새를 줄여보세요.';
  } else {
    return '';
  }
}
export function calculateEyeContactFeedback(score: number): string {
  if (score > 90) {
    return '훌륭한 시선 처리 능력을 보여주셨습니다! 청중과의 적극적인 아이 컨택을 통해 자신감과 신뢰감을 효과적으로 전달하셨습니다. 눈을 피하지 않고 청중을 주의 깊게 바라보는 태도는 발표의 설득력을 한층 높였으며, 발표자로서의 전문성을 돋보이게 했습니다. 앞으로도 이와 같은 자연스럽고 안정적인 시선 유지는 큰 강점이 될 것입니다.';
  } else if (score > 75) {
    return '전반적으로 안정적이고 자연스러운 시선 처리를 유지하셨습니다. 청중과의 연결이 잘 이루어졌으며, 발표 내용에 대한 신뢰감을 형성하는 데 긍정적인 역할을 했습니다. 다만, 중요한 메시지를 전달할 때는 더욱 의도적으로 시선을 활용하면 발표 효과를 더욱 높일 수 있습니다.';
  } else if (score > 50) {
    return '시선 처리가 비교적 안정적인 편이었지만, 아이 컨택의 지속성과 분산 면에서는 개선 여지가 보입니다. 청중에게 핵심 메시지를 전달할 때는 특정 청중을 바라보며 강조하거나, 전체 청중을 골고루 바라보는 습관을 들이면 더욱 자연스럽고 몰입도 높은 발표가 될 수 있습니다.';
  } else if (score > 25) {
    return '시선 처리가 제한적이며 발표 도중 눈의 움직임이 잦았던 점이 관찰되었습니다. 이는 청중과의 소통을 단절시키고 긴장한 인상을 줄 수 있으므로, 발표 연습 시 주요 청중에게 고르게 시선을 분산하는 훈련이 필요합니다. 발표를 녹화하여 본인의 시선 습관을 점검해보는 것도 도움이 됩니다.';
  } else {
    return '발표 중 시선을 자주 피하는 경향이 있었으며, 이는 청중에게 자신감이 부족하다는 인상을 줄 수 있습니다. 아이 컨택은 발표자와 청중 간의 신뢰를 형성하고 메시지를 명확히 전달하는 중요한 수단입니다. 거울 앞에서 발표를 연습하거나, 발표 영상을 촬영해보며 시선 처리에 집중해보세요. 반복적인 훈련을 통해 개선이 가능합니다.';
  }
}
